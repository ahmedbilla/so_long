#include "../../includes/so_long.h"

int	ft_strcmp(char *s1, char *s2)
{
	int i;
    if (s1 == NULL || s2 == NULL)
        return -1;
	
	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
		i++;
	return (s1[i] - s2[i]);
}